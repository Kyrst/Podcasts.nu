    <h2>Cookies</h2>
    En Cookie är en liten textfil som lagras i besökarens dator. Denna textfil kommer endast innehålla information för att underlätta
    för just ditt användande. Exempelvis var någonstans i ett avsnitt du är. För att vi på så sätt ska kunna erbjuda en bättre och mer
    lättanvänlig hemsida.
